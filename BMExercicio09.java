/* Escreva um programa que implemente um laço while que execute 20 ve-
zes, imprimindo o valor da variável x que inicialmente está com valor 10. */

public class BMExercicio09 {
    public static void main(String args[]) {
        int x = 10;
        while (x != 30) {
            System.out.println("Valor de X: " + x);
            x++;
        }
    }
}
